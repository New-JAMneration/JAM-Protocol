func TestGuaranteeSet(t *testing.T) {
	controller := NewGuaranteeController()

	// Create test guarantees
	guarantee1 := jamTypes.ReportGuarantee{
		Report: jamTypes.WorkReport{
			PackageSpec: jamTypes.WorkPackageSpec{
				Hash:         jamTypes.WorkPackageHash{0x01},
				Length:       42,
				ErasureRoot:  jamTypes.ErasureRoot{0x02},
				ExportsRoot:  jamTypes.ExportsRoot{0x03},
				ExportsCount: 69,
			},
			Context: jamTypes.RefineContext{
				Anchor:           jamTypes.HeaderHash{0x04},
				StateRoot:        jamTypes.StateRoot{0x05},
				BeefyRoot:        jamTypes.BeefyRoot{0x06},
				LookupAnchor:     jamTypes.HeaderHash{0x07},
				LookupAnchorSlot: 1,
			},
			CoreIndex: 1,
		},
		Slot: 100,
		Signatures: []jamTypes.ValidatorSignature{
			{
				ValidatorIndex: 1,
				Signature:      padSignature(jamTypes.Ed25519Signature{0x08}, 64),
			},
		},
	}

	guarantee2 := jamTypes.ReportGuarantee{
		Report: jamTypes.WorkReport{
			PackageSpec: jamTypes.WorkPackageSpec{
				Hash:         jamTypes.WorkPackageHash{0x11},
				Length:       43,
				ErasureRoot:  jamTypes.ErasureRoot{0x12},
				ExportsRoot:  jamTypes.ExportsRoot{0x13},
				ExportsCount: 70,
			},
			Context: jamTypes.RefineContext{
				Anchor:           jamTypes.HeaderHash{0x14},
				StateRoot:        jamTypes.StateRoot{0x15},
				BeefyRoot:        jamTypes.BeefyRoot{0x16},
				LookupAnchor:     jamTypes.HeaderHash{0x17},
				LookupAnchorSlot: 2,
			},
			CoreIndex: 2,
		},
		Slot: 101,
		Signatures: []jamTypes.ValidatorSignature{
			{
				ValidatorIndex: 2,
				Signature:      jamTypes.Ed25519Signature{0x18},
			},
		},
	}

	guarantees := []jamTypes.ReportGuarantee{guarantee1, guarantee2}

	// Test setting guarantees
	controller.Set(guarantees)

	// Verify length
	if controller.Len() != 2 {
		t.Fatalf("Expected 2 guarantees, got %d", controller.Len())
	}

	// Verify content
	if controller.Guarantees[0].Report.CoreIndex != 1 {
		t.Errorf("Expected first guarantee core index 1, got %d", controller.Guarantees[0].Report.CoreIndex)
	}

	if controller.Guarantees[1].Report.CoreIndex != 2 {
		t.Errorf("Expected second guarantee core index 2, got %d", controller.Guarantees[1].Report.CoreIndex)
	}

	// Test setting empty slice
	controller.Set([]jamTypes.ReportGuarantee{})
	if controller.Len() != 0 {
		t.Fatalf("Expected 0 guarantees after setting empty slice, got %d", controller.Len())
	}
}

func TestGuaranteeSort(t *testing.T) {
	controller := NewGuaranteeController()

	// Add guarantees in unsorted order
	guarantees := []jamTypes.ReportGuarantee{
		{
			Report: jamTypes.WorkReport{
				PackageSpec: jamTypes.WorkPackageSpec{
					Hash:         jamTypes.WorkPackageHash{0x03},
					Length:       43,
					ErasureRoot:  jamTypes.ErasureRoot{0x03},
					ExportsRoot:  jamTypes.ExportsRoot{0x03},
					ExportsCount: 70,
				},
				Context: jamTypes.RefineContext{
					Anchor:           jamTypes.HeaderHash{0x03},
					StateRoot:        jamTypes.StateRoot{0x03},
					BeefyRoot:        jamTypes.BeefyRoot{0x03},
					LookupAnchor:     jamTypes.HeaderHash{0x03},
					LookupAnchorSlot: 3,
				},
				CoreIndex: 3,
			},
			Slot: 103,
			Signatures: []jamTypes.ValidatorSignature{
				{
					ValidatorIndex: 3,
					Signature:      jamTypes.Ed25519Signature{0x03},
				},
			},
		},
		{
			Report: jamTypes.WorkReport{
				PackageSpec: jamTypes.WorkPackageSpec{
					Hash:         jamTypes.WorkPackageHash{0x01},
					Length:       41,
					ErasureRoot:  jamTypes.ErasureRoot{0x01},
					ExportsRoot:  jamTypes.ExportsRoot{0x01},
					ExportsCount: 68,
				},
				Context: jamTypes.RefineContext{
					Anchor:           jamTypes.HeaderHash{0x01},
					StateRoot:        jamTypes.StateRoot{0x01},
					BeefyRoot:        jamTypes.BeefyRoot{0x01},
					LookupAnchor:     jamTypes.HeaderHash{0x01},
					LookupAnchorSlot: 1,
				},
				CoreIndex: 1,
			},
			Slot: 101,
			Signatures: []jamTypes.ValidatorSignature{
				{
					ValidatorIndex: 1,
					Signature:      jamTypes.Ed25519Signature{0x01},
				},
			},
		},
		{
			Report: jamTypes.WorkReport{
				PackageSpec: jamTypes.WorkPackageSpec{
					Hash:         jamTypes.WorkPackageHash{0x02},
					Length:       42,
					ErasureRoot:  jamTypes.ErasureRoot{0x02},
					ExportsRoot:  jamTypes.ExportsRoot{0x02},
					ExportsCount: 69,
				},
				Context: jamTypes.RefineContext{
					Anchor:           jamTypes.HeaderHash{0x02},
					StateRoot:        jamTypes.StateRoot{0x02},
					BeefyRoot:        jamTypes.BeefyRoot{0x02},
					LookupAnchor:     jamTypes.HeaderHash{0x02},
					LookupAnchorSlot: 2,
				},
				CoreIndex: 2,
			},
			Slot: 102,
			Signatures: []jamTypes.ValidatorSignature{
				{
					ValidatorIndex: 2,
					Signature:      jamTypes.Ed25519Signature{0x02},
				},
			},
		},
	}

	for _, g := range guarantees {
		controller.Add(g)
	}

	// Verify the order after sorting
	expectedCoreIndices := []jamTypes.CoreIndex{1, 2, 3}
	for i, expected := range expectedCoreIndices {
		if controller.Guarantees[i].Report.CoreIndex != expected {
			t.Errorf("Guarantee at index %d has CoreIndex %d, expected %d",
				i, controller.Guarantees[i].Report.CoreIndex, expected)
		}
	}
}
